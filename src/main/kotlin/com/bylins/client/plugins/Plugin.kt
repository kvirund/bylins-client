package com.bylins.client.plugins

/**
 * Базовый интерфейс для всех плагинов.
 *
 * Плагины загружаются из JAR файлов и имеют жизненный цикл:
 * 1. onLoad() - инициализация ресурсов
 * 2. onEnable() - регистрация обработчиков, старт работы
 * 3. onDisable() - отписка от событий, остановка
 * 4. onUnload() - финальная очистка
 */
interface Plugin {
    /**
     * Уникальный идентификатор плагина (из plugin.yml)
     */
    val id: String

    /**
     * Вызывается при загрузке плагина (до enable).
     * Используется для инициализации ресурсов.
     */
    fun onLoad() {}

    /**
     * Вызывается при включении плагина.
     * Здесь регистрируются обработчики событий, команды и т.д.
     */
    fun onEnable()

    /**
     * Вызывается при выключении плагина.
     * Освобождение ресурсов, отписка от событий.
     */
    fun onDisable()

    /**
     * Вызывается при выгрузке плагина (после disable).
     * Финальная очистка.
     */
    fun onUnload() {}
}
